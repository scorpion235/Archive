// функция Rus заменяет в строке коды букв русского алфавита,
// обеспечивает их правильное отображение в окне консольного приложения
char* Rus(unsigned char* st)
{
   //  В ANSI русские буквы кодируются числами от 192 до 255,
   //  в ASCII - от 128 до 175 (А..Яа..п) и от 224 до 239 (р..я).

    unsigned char *p = st;

    while (*p)
    {
        if ( (*p >= 192) && (*p <= 239))
            *p -= 64;

        if ( ( *p >= 240) && (*p <= 255))
            *p -= 16;

        p++;
    }

    return st;
}



